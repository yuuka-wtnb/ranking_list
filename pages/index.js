import { images } from "@/next.config";
import Head from "next/head";
import React, { useState, useEffect } from "react";
import { Card,CardBody,CardText,CardTitle, Col, Input, Row } from "reactstrap";
import styles from './styles.module.css'

export default function Home() {
  const [data, Setdata] = useState([]);

  useEffect(() => {
    // データを取得するためのAPIエンドポイント
    const apiUrl = "https://next-training-shopping-api.vercel.app/api/ranking";

    //fetch関数を使ってAPIからデータを取得
    fetch(apiUrl)
      //
      .then((response) => response.json())
      .then((data) => {
        Setdata(data["ranking"]);
        console.log(data);
      })
      .catch((error) => {
        //errorの情報を渡す
        console.log("失敗", error);
      });
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
        <link
          rel="stylesheet"
          href="https://cdn.jsdelivr.net/npm/bootstrap@5.1.0/dist/css/bootstrap.min.css"
        />
      </Head>
      <main>
        <div>
          <h1>List of Items:</h1>
          <div className={styles['search-container']}>
          <div className={styles['search-bar']}>
      <input
        type="text"
        className={styles['search-input']}
        placeholder="検索..." />
      <button className={styles['search-button']}>検索</button>
      </div>
    </div>
            {data.map((item, index) => (
              <Row>
              <Col>
              <CardTitle className={styles.rank}>No.{item["rank"]}</CardTitle>
              <Card key={index} className={styles['card']}>
              <CardBody>
                <CardText className={styles.title}>{item["name"]}</CardText>
                <CardText>{item["price"]}</CardText>
                <CardText>{item["review"]}</CardText>
                <a href={item["url"]}>この商品のリンクへ</a>
                <div className={styles.item}>
                <img src={item["images"][0]["thumbnail"]} alt="Thumbnail"/>
                </div>
              </CardBody>
            </Card>
            </Col>
            </Row>
            ))}
        </div>
      </main>
    </>
  );
}
